            <h1>Código de conducta</h1>
            <h2>Nuestro compromiso</h2>
            <p>En el interés de fomentar una conferencia abierta y acogedora, nosotros como organizadores nos comprometemos a
                hacer de la participación en nuestro evento una experiencia donde se respete a todos los participantes y
                libre de acoso para todos ellos,
                independientemente de la edad, dimensión corporal, discapacidad, etnia, identidad y expresión de género,
                nivel de experiencia, nacionalidad, apariencia física, raza, religión, identidad u orientación sexual.</p>
            <h2>Nuestros estándares</h2>
            <p>Ejemplos de comportamiento que contribuyen a crear un ambiente positivo:</p>
                <ul>
                    <li>Uso de lenguaje amable e inclusivo</li>
                    <li>Respeto a diferentes puntos de vista y experiencias</li>
                    <li>Aceptación de críticas constructivas</li>
                    <li>Enfocarse en lo que es mejor para la comunidad y el evento</li>
                    <li>Mostrar empatía a otros miembros que participan en el evento</li>
                </ul>
                <p>Ejemplos de comportamiento inaceptable por participantes y ponentes:</p>
                <ul>
                    <li>Uso de lenguaje o imágenes sexuales y atención sexual no deseada</li>
                    <li>Comentarios insultantes o despectivos (<em>trolling</em>) y ataques personales o políticos</li>
                    <li>Acoso público o privado</li>
                    <li>Publicación de información privada de terceros sin su consentimiento, como direcciones físicas o electrónicas</li>
                    <li>Otros tipos de conducta que pudieran considerarse inapropiadas en un entorno profesional.</li>
                    <li>En el caso de ser ponente, una crítica con mala intención, menosprecio u agresión a otro profesional o ponente</li>
                </ul>
                <h2>Nuestras responsabilidades</h2>
            <p>Los organizadores son responsables de clarificar los estándares de comportamiento aceptable y se espera que
                tomen medidas correctivas y apropiadas en respuesta a situaciones de conducta inaceptable.</p>
                <p>Los organizadores tienen el derecho y la responsabilidad de prohibir temporal o permanentemente el acceso a esta edición o futuras ediciones del evento
                    a cualquier persona cuyo comportamiento sea inapropiado, amenazante, ofensivo o perjudicial e incluso denunciar dicho comportamiento a las autoridades oportunas.</p>
                <h2>Alcance</h2>
            <p>Este código de conducta aplica tanto al espacio donde se celebra el evento como a espacios públicos 
                donde un individuo comparta lugar con otras personas que participan en el evento.</p>
            <h2>Aplicación</h2>
            <p>Ejemplos de abuso, acoso u otro tipo de comportamiento inaceptable puede ser reportado directamente
                a la organización.
                Todas las quejas serán revisadas e investigadas, generando un resultado apropiado a las circunstancias.
                El equipo que forma parte de la organización está obligado a mantener confidencialidad de
                la persona que reportó el incidente.</p>

